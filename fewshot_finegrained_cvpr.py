class CUBDataset(Dataset):
    """CUB-200-2011 Dataset"""
    def __init__(self, root, transform=None):
        self.root = root
        self.transform = transform

        # Load image paths and labels
        images_file = os.path.join(root, 'images.txt')
        labels_file = os.path.join(root, 'image_class_labels.txt')

        with open(images_file, 'r') as f:
            self.images = [line.strip().split(' ')[1] for line in f]

        with open(labels_file, 'r') as f:
            self.labels = [int(line.strip().split(' ')[1]) - 1 for line in f]  # 0-indexed

        print(f"   CUB: {len(self.images)} images, {len(set(self.labels))} classes")

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img_path = os.path.join(self.root, 'images', self.images[idx])
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, self.labels[idx], idx


class FlowersDataset(Dataset):
    """Oxford Flowers-102 Dataset"""
    def __init__(self, root, transform=None):
        self.root = root
        self.transform = transform

        # Load labels from .mat file
        labels_mat = scipy.io.loadmat(os.path.join(root, 'imagelabels.mat'))
        self.labels = labels_mat['labels'][0] - 1  # 0-indexed

        # Image files are named image_00001.jpg to image_08189.jpg
        self.images = [f'image_{i:05d}.jpg' for i in range(1, len(self.labels) + 1)]

        print(f"   Flowers: {len(self.images)} images, {len(set(self.labels))} classes")

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img_path = os.path.join(self.root, 'jpg', self.images[idx])
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, int(self.labels[idx]), idx


class AircraftDataset(Dataset):
    """FGVC-Aircraft Dataset"""
    def __init__(self, root, transform=None):
        self.root = root
        self.transform = transform
        self.images_dir = os.path.join(root, 'data', 'images')

        # Load all variants (most fine-grained level)
        variants_file = os.path.join(root, 'data', 'variants.txt')
        with open(variants_file, 'r') as f:
            variants = [line.strip() for line in f]
        self.variant_to_idx = {v: i for i, v in enumerate(variants)}

        # Load all images and labels from trainval + test
        self.images = []
        self.labels = []

        for split in ['images_variant_trainval.txt', 'images_variant_test.txt']:
            split_file = os.path.join(root, 'data', split)
            if os.path.exists(split_file):
                with open(split_file, 'r') as f:
                    for line in f:
                        parts = line.strip().split(' ', 1)
                        img_name = parts[0] + '.jpg'
                        variant = parts[1]
                        self.images.append(img_name)
                        self.labels.append(self.variant_to_idx[variant])

        print(f"   Aircraft: {len(self.images)} images, {len(set(self.labels))} classes")

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img_path = os.path.join(self.images_dir, self.images[idx])
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, self.labels[idx], idx


