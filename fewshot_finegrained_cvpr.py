def create_few_shot_episode(features, labels, n_way, k_shot, n_query=15):
    """
    Create a single few-shot episode.

    Args:
        features: All features [N, D]
        labels: All labels [N]
        n_way: Number of classes per episode
        k_shot: Number of support examples per class
        n_query: Number of query examples per class

    Returns:
        support_X, support_y, query_X, query_y
    """
    unique_classes = np.unique(labels)

    # Sample n_way classes
    selected_classes = np.random.choice(unique_classes, n_way, replace=False)

    support_X, support_y = [], []
    query_X, query_y = [], []

    for new_label, cls in enumerate(selected_classes):
        # Get all indices for this class
        cls_indices = np.where(labels == cls)[0]

        # Ensure we have enough samples
        if len(cls_indices) < k_shot + n_query:
            # If not enough samples, sample with replacement for query
            chosen = np.random.choice(cls_indices, k_shot + n_query, replace=True)
        else:
            chosen = np.random.choice(cls_indices, k_shot + n_query, replace=False)

        support_idx = chosen[:k_shot]
        query_idx = chosen[k_shot:k_shot + n_query]

        support_X.append(features[support_idx])
        support_y.extend([new_label] * k_shot)
        query_X.append(features[query_idx])
        query_y.extend([new_label] * len(query_idx))

    return (np.concatenate(support_X), np.array(support_y),
            np.concatenate(query_X), np.array(query_y))


def evaluate_few_shot(features, labels, n_way, k_shot, n_query,
                      n_episodes, classifier_type='lr'):
    """
    Evaluate few-shot classification over multiple episodes.

    Args:
        classifier_type: 'lr' (logistic regression), 'ncc' (nearest class centroid),
                         'knn' (k-nearest neighbors)
    """
    accuracies = []

    for _ in range(n_episodes):
        support_X, support_y, query_X, query_y = create_few_shot_episode(
            features, labels, n_way, k_shot, n_query
        )

        # L2 normalize features (important for cosine-based methods)
        support_X = normalize(support_X)
        query_X = normalize(query_X)

        if classifier_type == 'ncc':
            # Nearest Class Centroid (Prototypical Networks style)
            centroids = []
            for c in range(n_way):
                class_features = support_X[support_y == c]
                centroids.append(class_features.mean(axis=0))
            centroids = np.array(centroids)
            centroids = normalize(centroids)

            # Cosine similarity (equivalent to dot product after normalization)
            similarities = query_X @ centroids.T
            preds = similarities.argmax(axis=1)

        elif classifier_type == 'lr':
            # Logistic Regression
            clf = LogisticRegression(
                C=10.0,
                max_iter=1000,
                solver='lbfgs',
                multi_class='multinomial',
                random_state=42
            )
            clf.fit(support_X, support_y)
            preds = clf.predict(query_X)

        elif classifier_type == 'knn':
            # K-Nearest Neighbors
            k = min(5, k_shot)  # Can't have more neighbors than support examples
            clf = KNeighborsClassifier(n_neighbors=k, metric='cosine')
            clf.fit(support_X, support_y)
            preds = clf.predict(query_X)

        else:
            raise ValueError(f"Unknown classifier: {classifier_type}")

        acc = accuracy_score(query_y, preds)
        accuracies.append(acc)

    mean_acc = np.mean(accuracies) * 100
    std_acc = np.std(accuracies) * 100
    ci95 = 1.96 * sem(accuracies) * 100

    return mean_acc, std_acc, ci95


